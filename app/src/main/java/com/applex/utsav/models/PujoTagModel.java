package com.applex.utsav.models;

public class PujoTagModel {

    private String pujoName;
    private String pujoUid;
    private String dp;

    public String getDp() {
        return dp;
    }

    public void setDp(String dp) {
        this.dp = dp;
    }

    public String getPujoName() {
        return pujoName;
    }

    public void setPujoName(String pujoName) {
        this.pujoName = pujoName;
    }

    public String getPujoUid() {
        return pujoUid;
    }

    public void setPujoUid(String pujoUid) {
        this.pujoUid = pujoUid;
    }
}
